    private void btnHapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnHapusMouseClicked
        try {
            int hapus = JOptionPane.showOptionDialog(null, "Anda yakin ingin menghapus data pengeluaran?", "Hapus",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
            if (hapus == JOptionPane.YES_OPTION) {
                if (this.selectedPengeluaranID == null) {
                    JOptionPane.showMessageDialog(null, "Pilih karyawan yang ingin dihapus");
                } else {
                    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_coolinarypos", "root", "");
                    PreparedStatement ps = con.prepareStatement("DELETE FROM tb_pengeluaran WHERE id_pengeluaran = '" + this.selectedPengeluaranID + "'");
                    ps.execute();
                    JOptionPane.showMessageDialog(null, "Data pengluaran berhasil dihapus");
                    idPengeluaran();
                    tampilkan();
                    reset();
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Tidak bisa dihapus karena terdapat data pengeluaran di transaksi");
            Logger.getLogger(Karyawan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnHapusMouseClicked

