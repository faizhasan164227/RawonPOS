    public static void idPengeluaran() {
        try {
            java.util.Date tgl = new java.util.Date();
            java.text.SimpleDateFormat kal = new java.text.SimpleDateFormat("ddMMyy");
            String tgl_format = kal.format(tgl);
            Connection con = (Connection) Config.configDB();
            String sql = "SELECT MAX(id_pengeluaran) AS id_pengeluaran FROM tb_pengeluaran WHERE id_pengeluaran LIKE 'TL" + tgl_format + "%'";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if (rs.next()) {
                String Nol = "";
                if (rs.getString("id_pengeluaran") == null) {
                    Nol = "000";
                    txt_idpengeluaran.setText("TL" + tgl_format + Nol + "1");
                } else {
                    String NoJual = rs.getString("id_pengeluaran").substring(8);
                    String AN = "" + (Integer.valueOf(NoJual) + 1);

                    if (AN.length() == 1) {
                        Nol = "000";
                    } else if (AN.length() == 2) {
                        Nol = "00";
                    } else if (AN.length() == 3) {
                        Nol = "0";
                    } else if (AN.length() == 4) {
                        Nol = "";
                    }
                    txt_idpengeluaran.setText("TL" + kal.format(tgl) + Nol + AN);//sesuaikan dengan variable namenya
                }
            }
            rs.close();
            con.close();
        } catch (Exception e) {
            e.printStackTrace();//penanganan masalah
        }
    }

    public static void tampilkan() {
        String[] judul = {"ID", "Tanggal", "Nama Pengeluaran", "Harga"};
        model = new DefaultTableModel(judul, 0);
        tb_pengeluaran.setModel(model);
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_coolinarypos", "root", "");
            ResultSet rs = con.createStatement().executeQuery("SELECT * FROM tb_pengeluaran");
            while (rs.next()) {
                String data[] = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)};
                model.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Pengeluaran.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

